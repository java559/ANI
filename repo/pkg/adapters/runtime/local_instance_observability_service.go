package runtime

import (
	"context"
	"fmt"
	"strings"
	"sync"
	"time"

	"github.com/google/uuid"
	"github.com/kubercloud/ani/pkg/ports"
)

type LocalInstanceObservabilityService struct {
	now             func() time.Time
	mu              sync.RWMutex
	sessions        map[string]ports.InstanceExecSessionRecord
	consoleSessions map[string]ports.InstanceConsoleSessionRecord
}

type InstanceObservabilityOption func(*LocalInstanceObservabilityService)

func WithInstanceObservabilityClock(now func() time.Time) InstanceObservabilityOption {
	return func(service *LocalInstanceObservabilityService) {
		if now != nil {
			service.now = now
		}
	}
}

func NewLocalInstanceObservabilityService(options ...InstanceObservabilityOption) *LocalInstanceObservabilityService {
	service := &LocalInstanceObservabilityService{
		now:             time.Now,
		sessions:        make(map[string]ports.InstanceExecSessionRecord),
		consoleSessions: make(map[string]ports.InstanceConsoleSessionRecord),
	}
	for _, option := range options {
		option(service)
	}
	return service
}

func (s *LocalInstanceObservabilityService) ListLogs(_ context.Context, request ports.InstanceObservationListRequest) (ports.InstanceLogListResult, error) {
	if err := validateInstanceObservationIdentity(request.TenantID, request.InstanceID); err != nil {
		return ports.InstanceLogListResult{}, err
	}
	now := s.now().UTC()
	items := []ports.InstanceLogEntry{
		{Timestamp: now.Add(-2 * time.Minute), Level: "info", Message: "local profile instance runtime is running", Container: "main", Stream: "stdout"},
		{Timestamp: now.Add(-90 * time.Second), Level: "warn", Message: "local profile does not attach to a real provider log stream", Container: "main", Stream: "stderr"},
		{Timestamp: now.Add(-30 * time.Second), Level: "debug", Message: "dev_profile observation generated by local adapter", Container: "sidecar", Stream: "stdout"},
	}
	items = filterLogs(items, request.Level)
	items = limitLogEntries(items, normalizeLimit(request.Limit, 100, 1000))
	return ports.InstanceLogListResult{Items: items, Total: len(items), DevProfile: instanceObservabilityDevProfile()}, nil
}

